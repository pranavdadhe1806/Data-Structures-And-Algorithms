import java.util.*;

public class TwoSum {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int a, b;
        System.out.println("Enter 1st Number: ");
        a = sc.nextInt();
        System.out.println("Enter 2nd Number: ");
        b = sc.nextInt();
        System.out.println("Sum: " + (a + b));
        sc.close();
    }
    
}
